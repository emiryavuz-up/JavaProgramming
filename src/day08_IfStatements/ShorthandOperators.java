package day08_IfStatements;

public class ShorthandOperators {

    public static void main(String[] args) {


        int x = 5;
        x += 3; // 5 + 3
        System.out.println(x);

        int y = 13;
        y -= 4; // 13-4
        System.out.println(y);

        int z = 52;
        z *= 2;
        System.out.println(z);

        int k = 962;
        k /= 5; // 962/5
        System.out.println(k);

    }


}
