package day05_Concatenation;

public class JavaPrintOut {
    /*
    Sample Pattern :
     J    a   v     v  a
   J   a a   v   v  a a
J  J  aaaaa   V V  aaaaa
 JJ  a     a   V  a     a
     */

    public static void main(String[] args) {

        System.out.println("\t\t J    a\t  v\t\t  v\t a");
        System.out.println("\t   J   a a\t v\t v\t  a a");
        System.out.println("\tJ  J  aaaaa\t  V V\t aaaaa");
        System.out.println("\t JJ  a\t   a   V    a     a");
    }
}
